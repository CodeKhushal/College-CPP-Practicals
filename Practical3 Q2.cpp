//Show the working of relational operations.
#include<iostream>
using namespace std;
int main()
{
    int a=5;
    int b=-3;
    cout<<"a<b is: \t"<<(a<b)<<endl;
    cout<<"a>b is: \t"<<(a>b)<<endl;
    cout<<"a<=b is: \t"<<(a<=b)<<endl;
    cout<<"a>=b is: \t"<<(a>=b)<<endl;
    cout<<"a==b is: \t"<<(a==b)<<endl;
    cout<<"a!=b is: \t"<<(a!=b)<<endl;
    cout<<"Thank You";
    return 0;
}
